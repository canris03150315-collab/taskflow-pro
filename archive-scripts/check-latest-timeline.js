// Check latest timeline records with full content
const Database = require('./node_modules/better-sqlite3');
const db = new Database('/app/data/taskflow.db');

console.log('Checking latest timeline records...');

const taskId = 'task-1767350493468-icvplvfqs';

try {
    // Get latest 3 timeline records
    const timeline = db.prepare('SELECT * FROM task_timeline WHERE task_id = ? ORDER BY timestamp DESC LIMIT 3').all(taskId);
    
    console.log('\n=== Latest 3 Timeline Records ===');
    
    if (timeline.length > 0) {
        timeline.forEach((entry, idx) => {
            console.log(`\n--- Record ${idx + 1} (Latest) ---`);
            console.log('ID:', entry.id);
            console.log('User ID:', entry.user_id);
            console.log('Content:', entry.content);
            console.log('Progress:', entry.progress);
            console.log('Timestamp:', entry.timestamp);
            console.log('Full entry:', JSON.stringify(entry, null, 2));
        });
    } else {
        console.log('No timeline records found!');
    }
    
    // Check if content contains user note
    const latestEntry = timeline[0];
    if (latestEntry) {
        console.log('\n=== Analysis ===');
        console.log('Latest content:', latestEntry.content);
        
        // Check for common note patterns
        const hasAutoGenerated = latestEntry.content.includes('進度更新為') || 
                                  latestEntry.content.includes('狀態更改為') ||
                                  latestEntry.content.includes('標題更改為');
        
        const contentParts = latestEntry.content.split(';').map(s => s.trim()).filter(s => s);
        
        console.log('Has auto-generated content:', hasAutoGenerated);
        console.log('Content parts:', contentParts);
        
        if (contentParts.length === 1 && hasAutoGenerated) {
            console.log('\n❌ PROBLEM: Only auto-generated content, no user note!');
        } else if (contentParts.length > 1) {
            console.log('\n✅ Multiple parts found, checking for user note...');
            contentParts.forEach((part, i) => {
                const isAutoGen = part.includes('進度更新為') || 
                                  part.includes('狀態更改為') ||
                                  part.includes('標題更改為');
                console.log(`Part ${i + 1}: ${isAutoGen ? '[AUTO]' : '[USER?]'} ${part}`);
            });
        }
    }
    
} catch (error) {
    console.error('ERROR:', error.message);
}

db.close();
